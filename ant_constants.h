#ifndef __PORTABLE_H__
#define __PORTABLE_H__

/* Colours */
/* Red */
#define  CR 0

/* Green */
#define  CG 1

/* Blue */
#define  CB 2

/* Yellow */
#define  CY 3

/* Black */
#define  CN 4

/* White */
#define  CW 5

/* Highest color number */
#define  MAXCOLOR 5

/* Orientation */
#define ON 0
#define OS 1
#define OE 2
#define OW 3

/* Rotations */
#define RL 0
#define RR 1

/* Ant attributes in bytes */
#define ANT_X 0
#define ANT_Y 4
#define ANT_O 8

/* Grid attributes in bytes */
#define GRID_WIDTH 0
#define GRID_HEIGHT 4
#define GRID_GRID 8

#endif /* __PORTABLE_H__ */

